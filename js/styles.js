document.getElementById('dorbell_title').addEventListener('click', () => {
    if(document.getElementById('dorbell_list').style.display != 'inherit') {
        document.getElementById('dorbell_list').style.display = 'inherit'
    } else {
        document.getElementById('dorbell_list').style.display = 'none'
    }
})


